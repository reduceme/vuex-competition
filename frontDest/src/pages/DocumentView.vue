        <p>&nbsp;&nbsp;&nbsp;&nbsp;（一）参赛范围：大赛面向全国核行业相关企事业、研究机构的青年科技人员和高校青年教师。</p>
        <p>&nbsp;&nbsp;&nbsp;&nbsp;（二）大赛主题：围绕选手所在单位的主业，以普及宣传清洁、安全、绿色核能为基本主题准备演讲稿。</p>
        <p>&nbsp;&nbsp;&nbsp;&nbsp;复赛主题：“绿色核能，美丽中国”<span class="english">(Theme for the Semi-Final: "Green Nuclear Energy • Beautiful China")</span></p>
        <p>&nbsp;&nbsp;&nbsp;&nbsp;以绿色核能为主题，描述核能发展及其对中国未来的
          影响、人民福祉的关系和建设美丽中国的美好愿景。<span class="english">(On the
        theme of green nuclear energy, describe the development
        of nuclear energy and its impact on the future of China,
        its relationship with the well-being of people and the
          good vision of building a beautiful China.),</span></p>
